package xn.core.util.excel;

/**
 * @Description: 级联下拉框隐藏域列信息
 * @author zhangjs
 * @date 2016年9月2日 上午9:46:37
 */
public class CascadeHiddenDataInfo {
    private String supName;

    private String alias;

    private String mergeCol;

    private String name;

    private String id;

    public String getSupName() {
        return supName;
    }

    public void setSupName(String supName) {
        this.supName = supName;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getMergeCol() {
        return mergeCol;
    }

    public void setMergeCol(String mergeCol) {
        this.mergeCol = mergeCol;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
